int ft_sqrt(int nb)
{
	int i;

	i = 0;
	while (i < nb)
	{
		if (nb < 0)
			return (0);
		else
		{
			if (i * i == nb)
				return (i);
			else
				i++;
		}
	}
	return (i);
}
